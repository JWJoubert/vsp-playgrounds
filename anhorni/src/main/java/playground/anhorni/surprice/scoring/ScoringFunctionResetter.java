package playground.anhorni.surprice.scoring;

import org.matsim.core.controler.Controler;
import org.matsim.core.controler.events.IterationEndsEvent;
import org.matsim.core.controler.listener.IterationEndsListener;


/**
 * 
 * @author anhorni
 */
public class ScoringFunctionResetter implements IterationEndsListener {

	@Override
	public void notifyIterationEnds(IterationEndsEvent event) {
		Controler controler = event.getControler();
		controler.getPlansScoring().getPlanScorer().reset(event.getIteration()); // TODO: check if iteration + 1 required
	}
}
package playground.jbischoff.BAsignalsDemand;

import java.io.IOException;
import java.util.Iterator;

import org.geotools.data.FeatureSource;
import org.geotools.feature.Feature;
import org.matsim.core.utils.gis.ShapeFileReader;

public class JBShpExtractor {
	public static void main(String [] args) throws IOException {
		String zonesFile = "/Users/JB/Desktop/BA-Arbeit/sim/spn.shp";
		FeatureSource fts = ShapeFileReader.readDataFile(zonesFile); //reads the shape file in
		Iterator<Feature> it = fts.getFeatures().iterator();

		while (it.hasNext()) {
			Feature ft = it.next(); 
			System.out.println(ft.getFeatureType());
			System.out.println(ft.getAttribute("the_geom"));
			
	}		}
}
